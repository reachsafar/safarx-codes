def factorial():   
    #multiplication program 
    #welcome
    print('!!!Welcome to SAFARX factorial!!!')

    #user input
    num=int(input('Enter the no. :  '))

    #define fact value
    fact=(1)

    #loop
    for i in range(1,num+1):
        fact = fact*i                                                  

    #display result of factorial
    print('Factorial of ',num,'is',fact)

